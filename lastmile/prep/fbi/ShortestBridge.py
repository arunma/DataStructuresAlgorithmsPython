from typing import List


class ShortestBridge:
    def shortestBridge(self, A: List[List[int]]) -> int:
        firstIsland = None
        R = len(A)
        C = len(A[0])

        for r in range(R):
            for c in range(C):
                if A[r][c] == 1:
                    firstIsland = (r, c)
                    break

        queue = []
        seen=set()
        self.findAllPartsOfIsland(A, R, C, firstIsland[0], firstIsland[1], queue, seen)
        return self.findNearestIsland(A, R, C, queue)

    def findNearestIsland(self, A, R, C, queue) -> int:
        seen=set()
        while queue:
            ((cr, cc), level) = queue.pop(0)
            seen.add((cr,cc))
            if A[cr][cc] == 1:
                return level - 1

            npairs = [(cr + 1, cc), (cr - 1, cc), (cr, cc + 1), (cr, cc - 1)]
            for nr, nc in npairs:
                if -1 < nr < R and -1 < nc < C and (nr,nc) not in seen:
                    queue.append(((nr, nc), level + 1))
        return -1

    def findAllPartsOfIsland(self, A, R, C, r, c, queue, seen):
        queue.append(((r, c), 0))
        seen.add((r,c))
        A[r][c] = 0
        npairs = [(r + 1, c), (r - 1, c), (r, c + 1), (r, c - 1)]
        for nr, nc in npairs:
            if -1 < nr < R and -1 < nc < C and A[nr][nc] == 1 and (nr,nc) not in seen:
                self.findAllPartsOfIsland(A, R, C, nr, nc, queue, seen)


