import string
class ReplaceAllQsToAvoidConsecutiveRepeatingChars:
    def modifyString(self, s: str) -> str:
        if not s:
            return s

        alphas=list(string.ascii_lowercase)
        slist=list(s)

        for i, c in enumerate(slist):
            if c == '?':
                for a in alphas:
                    if (i>0 and a == slist[i - 1]) or (i < len(slist) - 1 and a == slist[i + 1]):
                        continue
                    slist[i] = a
                    break

        return ''.join(slist)



if __name__ == '__main__':
    init = ReplaceAllQsToAvoidConsecutiveRepeatingChars()
    print(init.modifyString("?zs")) #azs
    print(init.modifyString("ubv?w")) #ubvaw
    print(init.modifyString("??yw?ipkj?")) #acywaipkja
